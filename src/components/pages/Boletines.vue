            <br /><br />
            Por ejemplo, en el nuevo enfoque de eficacia que tiene la Norma, ya
            no solo se debe llevar a cabo la observación electoral, toda vez que
            incorpora la auditoría de los procesos electorales, para potenciar
            la confianza de los ciudadanos, garantizar la integridad del
            servicio electoral y proporcionar resultados confiables, la norma
            <b>ISO/TS 54001:2019</b> comprende 8 subprocesos que garantizan el
            correcto desempeño de un organismo electoral, éstos son:
            <br /><br />
            <ol>
              <li>
                <b>INSCRIPCIÓN ELECTORAL:</b> Se recibe, revisa y verifica la
                información sustancial de los ciudadanos que van a emitir el
                voto.
              </li>
              <li>
                <b>INCRIPCIÓN DE ORGANIZACIONES POLÍTICAS Y DE CANDIDATOS:</b>El
                órgano electoral se encarga de recibir y validar que la
                información de quienes deseen contender en las elecciones,
                cumpla con los requerimientos legales establecidos.
              </li>
              <li>
                <b>LOGISTICA ELECTORAL:</b> Contempla todo el proceso de
                organización y realización de una elección; preparar, resguardar
                y distribuir el material para la votación (papeletas y registros
                electorales), designación y capacitación de funcionarios
                electorales, finalizando con la apertura eficiente y operación
                continua de las mesas de votación.
              </li>
              <li>
                <b>EMISIÓN DEL VOTO:</b> Garantiza que el procedimiento de
                votación se apegue al marco legal, regulatorio y normativo.
                Verificando la identidad los electores, protegiendo la
                integridad de las urnas y garantizando el acceso de los
                observadores acreditados, así como de los representantes,
                autorizados por los candidatos, de observar el proceso de
                votación.
              </li>
              <li>
                <b>ESCRUTINIO Y DECLARACIÓN DE RESULTADOS:</b> Se tiene que
                determinar el número total de votantes; se verifican y
                clasifican los votos en grupos que representen las diferentes
                opiniones políticas; se cuentan los votos a la vista de todos
                los presentes; se llenan las hojas de resultados o la
                declaración de votos; y finalmente se tabula el resultado para
                anunciar los resultados en cuanto estén disponibles. Un aspecto
                sustancial en este subproceso es que también se debe tener un
                conteo del número de papeletas sin usar.
              </li>
              <li>
                <b>EDUCACIÓN ELECTORAL:</b> Se debe promover en los votantes, la
                importancia de su participación en el proceso electoral, así
                como informarles, de manera detallada, sobre los subprocesos de
                inscripción de los candidatos.
              </li>
              <li>
                <b>FISCALIZACIÓN DEL FINANCIAMIENTO DE CAMPAÑAS ELECTORALES:</b>
                Es responsabilidad del organismo electoral realizar el
                seguimiento de las fuentes de financiamiento, públicas o
                privadas, de los gastos de quienes participan en el proceso de
                elección, así como verificar que el acceso a los medios de
                comunicación sea para fines exclusivamente de campaña.
              </li>
              <li>
                <b>RESOLUCIÓN DE CONFLICTOS ELECTORALES:</b> En cuanto a las
                quejas electorales que se pudieran presentar, el organismo debe
                de: recibir y revisar la documentación; asegurar el cumplimiento
                con todos los procedimientos requeridos por ley, tanto antes
                como después de la resolución; coordinar y cooperar con los
                organismos judiciales responsables de resolución de conflictos;
                y adoptar e implementar la resolución.
              </li>
